package it.polimi.tiw.projects.beans;

public class User {
	private String code;
	private String role;
	private String name;

	public String getCode() {
		return code;
	}

	public String getRole() {
		return role;
	}

	public String getName() {
		return name;
	}
	
	public void setCode(String i) {
		code = i;
	}

	public void setRole(String r) {
		role = r;
	}

	public void setName(String n) {
		name = n;
	}

}

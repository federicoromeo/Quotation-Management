package it.polimi.tiw.projects.beans;

public class User {
	private int id;
	private String role;
	private String username;

	public int getId() {
		return id;
	}

	public String getRole() {
		return role;
	}

	public String getUsername() {
		return username;
	}
	
	public void setId(int i) {
		id = i;
	}

	public void setRole(String r) {
		role = r;
	}

	public void setUsername(String un) {
		username = un;
	}

}
